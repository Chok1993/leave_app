def to_date(s):
    """‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏õ‡πá‡∏ô Date Object (‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡∏´‡∏•‡∏≤‡∏¢ format)"""
    if pd.isna(s): return pd.NaT
    try:
        return pd.to_datetime(s).date()
    except (ValueError, TypeError):
        return pd.NaT

def to_time(s):
    """‡πÅ‡∏õ‡∏•‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏õ‡πá‡∏ô Time Object (‡∏£‡∏≠‡∏á‡∏£‡∏±‡∏ö‡∏´‡∏•‡∏≤‡∏¢ format)"""
    if pd.isna(s): return None
    try:
        return pd.to_datetime(str(s)).time()
    except (ValueError, TypeError):
        return None

